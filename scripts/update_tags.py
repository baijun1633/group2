def main():
    conn = pymysql.connect(**DB_CONFIG)
    cursor = conn.cursor(pymysql.cursors.DictCursor)

    # 查询所有书籍
    cursor.execute("""
        SELECT b.book_id, b.title, b.author, c.name as category
        FROM books b
        JOIN categories c ON b.category_id = c.category_id
        ORDER BY b.book_id
    """)
    books = cursor.fetchall()

    print(f"共 {len(books)} 本书需要更新 tag")

    updated = 0
    for book in books:
        tags = generate_tags(book['title'], book['author'], book['category'])
        tags_json = json.dumps(tags, ensure_ascii=False)

        cursor.execute(
            "UPDATE books SET tags = %s WHERE book_id = %s",
            (tags_json, book['book_id'])
        )
        updated += 1

        if updated % 100 == 0:
            print(f"  已更新 {updated}/{len(books)} 本...")
            conn.commit()

    conn.commit()
    print(f"完成！共更新 {updated} 本书的 tag")

    # 验证
    cursor.execute("SELECT COUNT(*) as cnt FROM books WHERE tags IS NOT NULL")
    result = cursor.fetchone()
    print(f"验证：{result['cnt']} 本书已有 tag")

    # 打印几个示例
    cursor.execute("""
        SELECT b.title, b.author, c.name as category, b.tags
        FROM books b JOIN categories c ON b.category_id = c.category_id
        WHERE b.tags IS NOT NULL
        ORDER BY RAND() LIMIT 10
    """)
    print("\n随机示例：")
    for row in cursor.fetchall():
        print(f"  {row['title']} ({row['author']}) [{row['category']}]: {row['tags']}")

    cursor.close()
    conn.close()


